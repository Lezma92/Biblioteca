    public void busquedaweb(String addr) throws Exception {
        // build a URL
        String s = "https://www.googleapis.com/books/v1/volumes?q=";
        s += URLEncoder.encode(addr, "UTF-8");
        System.out.println(s);
        URL url = new URL(s);
        System.out.println(url);
        // read from the URL
        Scanner scan = new Scanner(url.openStream());
        String str = new String();
        System.out.println(str);
        while (scan.hasNext()) {
            str += scan.nextLine();
            //System.out.println(str);
        }
        scan.close();
      System.out.println(str);
//         build a JSON object
        JSONObject obj = new JSONObject(str);
//        if (!obj.getString("kind").equals("OK")) {
//            return;
//        }

//         get the first result
        JSONObject numero_venta = obj.getJSONArray("items").getJSONObject(0);
        System.out.println(numero_venta.getString("id").toUpperCase());
        num.setText(numero_venta.getString("id").toUpperCase());
        JSONObject loc = numero_venta.getJSONObject("volumeInfo");
        System.out.println(loc.getString("title"));
        nom.setText(loc.getString("title"));
        
       // au.setText(""+numero_venta.getJSONArray(""));
        System.out.println(loc.getString("publishedDate"));
        date.setText(loc.getString("publishedDate"));
        
        System.out.println(loc.getString("title"));
    }
