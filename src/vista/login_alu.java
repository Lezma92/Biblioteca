    private void ingresar() {
        String nombre = txt_nombre.getText().trim();
        String matricula = txt_matricula.getText().trim();
        if (!nombre.equals("") && !matricula.equals("")) {
            String respuesta = opc.accesoAlumno(Integer.parseInt(matricula), nombre);
            if (respuesta.equals("Correcto")) {
                JOptionPane.showMessageDialog(rootPane, "¬°Bienvenido " + nombre + "!");
                new principal(2, Integer.parseInt(matricula)).setVisible(true);
                this.dispose();
            } else if (respuesta.equals("Mal")) {
                JOptionPane.showMessageDialog(rootPane, "El nombre o matr√≠cula son incorrectos\n por favor verifique sus datos");
            } else {
                JOptionPane.showMessageDialog(rootPane, respuesta);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Favor de ingresar los datos correspondiente ");
        }
    }
